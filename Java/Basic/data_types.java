package Java.Basic;

public class data_types {
    public static void main(String[] args) {
        int number = 0;
        double number2 = 1.5;
        char character = 'x';
        String name = "Arthur";

        System.out.print("\n");
        System.out.print("This is a variable of a int type: "+number+"\n");
        System.out.print("This is a variable of a double type: "+number2+"\n");
        System.out.print("This is a variable of a char type: "+character+"\n");
        System.out.print("This is a variable of a string type: "+name+"\n");
        System.out.print("\n");
    }
}
